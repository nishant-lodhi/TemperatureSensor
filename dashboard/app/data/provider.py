"""Data provider — protocol, factory, and caching.

All dashboard pages call get_provider(client_id) to get a client-scoped
data source.  Instances are cached per client_id.
"""

from __future__ import annotations

from typing import Optional, Protocol


class DataProvider(Protocol):
    def get_all_sensor_states(self) -> list[dict]: ...
    def get_readings(self, device_id: str, since_iso: str, until_iso: str | None = None) -> list[dict]: ...
    def get_forecast(self, device_id: str, horizon: str) -> dict | None: ...
    def get_forecast_series(self, device_id: str, horizon: str, steps: int) -> list[dict]: ...
    def get_compliance_history(self, days: int) -> list[dict]: ...
    def get_all_devices(self) -> list[str]: ...
    def get_zones(self) -> list[str]: ...
    def get_live_alerts(self) -> list[dict]: ...
    def get_alert_history(self, device_id: Optional[str], days: int) -> list[dict]: ...
    def dismiss_alert(self, device_id: str, alert_type: str) -> None: ...
    def send_alert_note(self, device_id: str, alert_type: str, context: dict) -> bool: ...


_providers: dict[str, DataProvider] = {}


def get_provider(client_id: str | None = None) -> DataProvider:
    """Return a DataProvider scoped to the given client_id (cached)."""
    cid = client_id or "default"
    if cid in _providers:
        return _providers[cid]
    from app.data.hybrid_provider import HybridProvider
    _providers[cid] = HybridProvider(cid)
    return _providers[cid]
